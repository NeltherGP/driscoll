/**
 * Interface representing a user in the application.
 * @interface User
 */
export interface User{
    /**
   * The first name of the user.
   * @type {string}
   */
    FirstName: String,
    /**
    * The last name of the user.
    * @type {string}
    */
    LastName: String,
    /**
    * The email address of the user.
    * @type {string}
    */
    Email: String,
    /**
   * The creation date of the user.
   * @type {string}
   * 
   */
    Created: String

}